export const getFreeDirectories = () => {
  return DIRECTORY_SEED_DATA.filter(dir => dir.price === 0);
};

export const getDirectoriesStats = () => {
  const total = DIRECTORY_SEED_DATA.length;
  const byTier = {
    tier1: DIRECTORY_SEED_DATA.filter(d => d.tier_required === 1).length,
    tier2: DIRECTORY_SEED_DATA.filter(d => d.tier_required === 2).length,
    tier3: DIRECTORY_SEED_DATA.filter(d => d.tier_required === 3).length,
    tier4: DIRECTORY_SEED_DATA.filter(d => d.tier_required === 4).length,
  };
  const byDifficulty = {
    easy: DIRECTORY_SEED_DATA.filter(d => d.difficulty === 'Easy').length,
    medium: DIRECTORY_SEED_DATA.filter(d => d.difficulty === 'Medium').length,
    hard: DIRECTORY_SEED_DATA.filter(d => d.difficulty === 'Hard').length,
  };
  const avgDA = Math.round(DIRECTORY_SEED_DATA.reduce((sum, d) => sum + d.domain_authority, 0) / total);
  const freeDirectories = DIRECTORY_SEED_DATA.filter(d => d.price === 0).length;

  return {
    total,
    byTier,
    byDifficulty,
    averageDomainAuthority: avgDA,
    freeDirectories,
    paidDirectories: total - freeDirectories
  };
};

